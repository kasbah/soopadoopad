def getColour(val):
	val &= 0b110011
	val |= 0b000100
	return colour_map[val][0]

def getColourName(val):
	val &= 0b110011
	val |= 0b000100
	return colour_map[val][1]

def colourStrToMIDI(name):
	for key in colour_map:
		if colour_map[key][1] == name:
			return key

